// https://stackoverflow.com/a/12968117
const isPort = (str) => {
  const expression = /^([0-9]{1,4}|[1-5][0-9]{4}|6[0-4][0-9]{3}|65[0-4][0-9]{2}|655[0-2][0-9]|6553[0-5])$/;
  return expression.test(str);
};

export default isPort;
